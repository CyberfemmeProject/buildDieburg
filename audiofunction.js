var toggle = "off";

pin.onclick = function(){
  if(toggle == "off"){
    pin.setAttribute("src","https://cdn.onlinewebfonts.com/svg/img_465535.png");
    toggle = "on";
    testMusic.play();
  }else {
    pin.setAttribute
    ("src","map.png");
    toggle = "off";
    testMusic.pause();
  }
}

